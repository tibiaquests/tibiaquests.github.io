 The Arcanum: Annihilon
The Arcanum is a place with Warlocks, Liches, several Hand of Cursed Fate, Braindeaths, Vampires, Undead Dragons, Efreets, Diabolic Imps, Demons and possibly more. The path is a strait line through the 3 rooms before you reach the teleport to enter the Soul Wells.

The Soul Wells is full of Betrayed Wraiths, Spectres, Lost Souls, Phantasms, Demons and possibly more. Head south, while sticking the western wall to avoid the majority of the spawn. Continue to head south until you find the teleporter to Annihilon.

