 The Blood Halls: Madareth
Here you will face a great number of Dark Torturers, Betrayed Wraiths, Lost Souls and Hand of Cursed Fate. Kill the Dark Torturers first.

Keep going north and west to the center of the room to find stairs. Up these stairs is an obscene amount of the same previously mentioned creatures. At least two very high level knights (190+) must try to go up the stairs and to corners with the monsters. Immediately, sio'ers must go up and sio the blockers or just let a mage (150+) stair jump with Any Ue (Recommended Rage of the skies or Eternal Winter).

Go west, then south and you will find another stairs. You can use the same tatic of previous stairs here. Go east, then north and you will find a teleport.

Going through this teleport and you will be in a room with several Juggernauts, Hellfire Fighters, Plaguesmiths, Furies and Destroyers. Shooters must Magic Wall to make a barrier or many monsters will come at same time.

After kill all monsters, go to the southeast corner of the room and you will find the teleport to Madareth's Ward. Sorcerers and Druids can use Icicle Runes and Paladins can use Assassin Stars and Divine Missiles to kill Madareth.

Once he is dead, go through the teleport to be teleported to the next throne.
