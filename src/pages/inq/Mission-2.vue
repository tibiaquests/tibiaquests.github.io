 Mission 2: EclipseJump to TranscriptJump to TOC
Henricus tells you to get The Witches' Grimoire, he sends you to Femor Hills where you can fly to the witches' mountain, say Eclipse to Uzon and he will take you there.
You are in a Protection Zone when you arrive. Go down the ramp to the witch's camp.

Beware here, because you can find up to 8 Witches, 2 Ghouls and a Dark Magician. Also some 2 Cats, 2 Rats, and 2 Frogs will be there to annoy you.
It's recommended to use Sudden Death runes and Death Strike here since the Witches are weak to Death Damage.

Use the vial of holy water that he gives you on the big cauldron (some kind of an explosion will appear on the cauldron) and open the chest to your left, then bring the witches' grimoire to Henricus.

