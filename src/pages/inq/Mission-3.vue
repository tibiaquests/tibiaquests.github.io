 Mission 3: Vampire Hunt
Ask for mission to Henricus. He wants you to find the Dwarfish Vampire Hunter, Storkus, located west of the Dwarf Bridge here. It's good idea bring your 20 Vampire Dusts with you to save some time.

Way from the dwarf bridge

Once you found Storkus, he wants 20 Vampire Dusts. Give to him and ask again for a mission.

Now he wants you to kill a vampire lord, The Count in the Green Claw Swamp, The Count is located near to the Blood Herb Quest, here. You will need a Machete or equivalent tool to cut the Jungle Grass on your way there.

The Inquisition Vampire Hunter Map.png

When you go down the hole walk to the right and go down the next stairs. You'll see a buried coffin surrounded by 3 counters. To summon The Count, you must "use" the coffin in the center of the room. Kill it and bring The Ring of the Count to Storkus.

Note: There is a way to make this mission easier, just put 1 Garlic Necklace on each counter before using the coffin. Now use the coffin and when The Count spawns, he will be The Weakened Count (just like one regular vampire), making your work much easier.

Note: The count can easily kill a 85 mage in one turn with summons. It is recommend to use the garlic necklace trick.

