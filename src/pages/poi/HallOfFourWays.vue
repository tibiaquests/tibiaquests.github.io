Hall of the Four Ways.
Paladins go south-east to open the first flame.
Knights go north to open the second flame.
Sorcerers go south to open the third flame.
Druids go north-east east to open the fourth flame.
The hall is located here. There are 4 paths with purple fires. One player of each vocation must cross a path through the fire. The fires will harm you for 300 hp if you are on the correct path, but if you are not on the right path the damage will increase, eventually leading to death. Players who take the wrong path will receive a message indicating this.
Each vocation needs to rope up a hole into a room with 3 Creatures:

Paladins - 1 Spectre and 2 Nightmares
Knights - 2 Diabolic Imps and 1 Spectre
Sorcerers - 1 Spectre and 2 Nightmares
Druids - 1 Spectre, 1 Diabolic Imp and 1 Nightmare
These monsters do not have to be killed, but you must pull a lever in the room at the end of the path. Dwarven Rings are highly recommended for all players.

Tip: Knights and paladins will have difficulty bringing down the spectre. It is recommended to run it.
Tip: Druids can SD the Diabolic Imp and then kill the Spectre.
Once all 4 levers have been pulled, the fire walls will have disappeared and the team can proceed through to the main area, also called "PoI levers". Make sure your entire team is through the walls before continuing as the fire walls will close once the stones disappear (in the next part).

Note: If your team needs to leave and the fire walls have closed, you can exit via the emergency exit on the east side of the flames. This will take you to the west side, you will have to exit via the dragon lair.
