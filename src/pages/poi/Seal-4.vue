Throneroom of Verminor, the Plaguelord.
In this throneroom you will meet Defilers, Plaguesmiths, Sons of Verminor, Blightwalkers and possibly The Plasmother. Go south and follow the way to reach the throne, step on it twice to absorb some of Verminors spirit, then return to the teleporter.

Note: There are some invisible bridges (poison) near the throne, you will have to walk over them to reach the throne.
